public class LatihanVariabel{
    public static void main(String[] args)
     {//deklarasi variabel x dan y
        int x = 100;
        int y = 200;
        int  total = x + y;

      //menampilkan nilai x dan y
      System.out.println("Nilai x = " + x +" dan nilai y = " + y);
      System.out.println("Nilai x ditambah nilai y= " + (x+y));  
      System.out.println("Nilai x ditambah nilai y= " + total);  
      System.out.println("Total= " + x +"+"+ y+"="+total);
    }
}
